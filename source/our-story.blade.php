<h2>Swimming against the stream: The Boston Volunteers</h2>

<p>They said it was impossible. They said it couldn't be done. Create a way to organize and empowers thousands of caring people to help fellow Bostonians in need? And do it in a no-hassle, no commitment way where folks can easily choose where and how they want to help?</p>
 
<p>When we started the Boston Volunteers, we bucked all the trends. No mandatory training sessions for new people. No large corporate structures or high costs. Simply a bunch of good-natured and hard working people wanting to make our little corner of the world a better place. Why?</p>
 
<p>Because we believe in better.</p>
 
<p>With nothing more than word-of-mouth reputation, Meetup.com as a springboard, and a knack for gathering some of the coolest people around, the Boston Volunteers have experienced exponential and geometric growth. Who would have imagined that in our first three years we'd have more than forty seven hundred good-hearted Boston Volunteers standing up and fighting for better? Not us, for sure!</p>
 
<p>While we are so incredibly grateful for all the volunteers who are already part of our community, there are still countless people in our neighbourhood who find themselves in need. That's why we need you. All you need is a caring heart and a willingness to share a little bit of your valuable time.</p>
 
<p>Join us and spend an evening in a Boston soup kitchen; help tutor a single, local mom so she can earn her GED; rebuild your neighborhood playground so kids can play safely; kayak and clean the Charles to better our environment; build a home for a local family; enjoy time with a lonely elderly grandmother. The variety is endless and refreshing. You choose where and when. There will be a convenient volunteer celebration for you. Don't miss this easy opportunity to help and  build community.</p>
 
<p>While the Boston Volunteers have a well-earned reputation for having hard working and dependable folks, we're also known to have a pretty good time doing so. Beyond having fun while helping others, we also celebrate a variety of social events in your neighbourhood where we simply spend a little time relaxing and getting to know each other.</p>
 
<p>Volunteer enough and you will make some meaningful and lasting friendships with some truly great people. I've built a growing and meaningful community. I’ve even met my beautiful wife at an event!</p>
 
<p>You choose where and when you would like to volunteer, you choose what new ideas and opportunities to bring to our attention. And if you have a passion and there's a need, we'll get you set up to lead your own volunteer event. We revere the can-do spirit.</p>
 
<p>We seek to keep on getting better. Do you have any ideas for us? Please share.</p>
<p>We are relentlessly pursuing perfection.</p>
 
<p>Are you ready to partner with a community of like-minded folks as we help our neighbors in need? Set-out to make our city cleaner, safer and better.</p>
<p>Learn more about our popular nonprofit, take a peek at upcoming volunteer events, sign up when there's a space, and start making a difference.</p>
 
<p>Let’s get to work!</p>
 
<p>The Boston Volunteers</p>
